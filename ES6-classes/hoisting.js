// Function Declaration
function sayHello() {}

// Function expression
const sayGoodMorning = function () {};

/*
  the difference between Function Declarations and Function expressions is that
  Function Declarations are hoisted i.e moved to the TOP at runtime
  while Function expressions are not
*/

// Class Declaration
class Circle {}

// Class expression
const Circle = class {};

/*
  Unlike Functions, classes are not hoisted
*/
